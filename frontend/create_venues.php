<script>
    $(document).ready(function() {
        $('#create-venue-form').submit(function(event) {
            event.preventDefault();
            var formData = $(this).serialize();
            $.ajax({
                type: 'POST',
                url: '../backend/venues.php',
                data: formData,
                success: function(response) {
                    if (response === 'success') {
                        window.location.href = 'list_venues.php';
                    } else {
                        alert('Error creating venue');
                    }
                }
            });
        });
    });
</script>


**venues.php (backend)**

<?php
// Include database connection
include 'db.php';

// Check if form data is submitted
if (isset($_POST['name']) && isset($_POST['address']) && isset($_POST['city']) && isset($_POST['state']) && isset($_POST['zip']) && isset($_POST['phone']) && isset($_POST['description'])) {
    // Prepare SQL query
    $sql = "INSERT INTO venues (name, address, city, state, zip, phone, description) VALUES (?, ?, ?, ?, ?, ?, ?)";
    $stmt = $conn->prepare($sql);
    $stmt->bind_param("sssssss", $_POST['name'], $_POST['address'], $_POST['city'], $_POST['state'], $_POST['zip'], $_POST['phone'], $_POST['description']);
    $stmt->execute();
    $stmt->close();
    echo 'success';
} else {
    echo 'Error creating venue';
}
?>